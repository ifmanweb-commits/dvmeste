// Вспомогательные функции
function randomElement<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)]
}

function randomInt(min: number, max: number): number {
  return Math.floor(Math.random() * (max - min + 1)) + min
}

// Транслитерация кириллицы в латиницу
function transliterate(text: string): string {
  const ruToEn: Record<string, string> = {
    'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo',
    'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm',
    'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u',
    'ф': 'f', 'х': 'h', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'sch', 'ъ': '',
    'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya',
    ' ': '-', '_': '-', '/': '-'
  }
  
  return text.toLowerCase().split('').map(char => ruToEn[char] || char).join('')
}

function generateSlug(title: string): string {
  // Сначала транслитерируем, затем удаляем всё кроме латиницы, цифр и минуса
  return transliterate(title)
    .replace(/[^a-z0-9\s-]/gi, "")
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 50) + "-" + randomInt(1000, 9999)
}

function generateExcerpt(content: string): string {
  return content
    .replace(/<[^>]+>/g, "")
    .slice(0, 200) + "..."
}

// Копирование файла
async function copyFile(src: string, dest: string): Promise<void> {
  try {
    await fs.copyFile(src, dest)
    console.log(`  📁 Скопировано: ${src} → ${dest}`)
  } catch (error) {
    console.error(`  ❌ Ошибка копирования ${src}:`, error)
    throw error
  }
}

// Создание директории
async function ensureDir(dir: string): Promise<void> {
  await fs.mkdir(dir, { recursive: true })
}

// Создание изображения для статьи
async function createArticleImage(articleId: string, fileName: string): Promise<void> {
  const srcPath = join(process.cwd(), 'tmp-photos', fileName)
  const destDir = join(process.cwd(), 'public', 'files', 'articles', articleId)
  const destPath = join(destDir, fileName)
  
  await ensureDir(destDir)
  await copyFile(srcPath, destPath)
  
  await prisma.articleImage.create({
    data: {
      articleId,
      url: `/files/articles/${articleId}/${fileName}`,
      storagePath: `/files/articles/${articleId}/${fileName}`
    }
  })
}

